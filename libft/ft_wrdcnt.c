#include "libft.h"

int		ft_wrdcnt(char const *s, char c)
{
	int		i;
	char	*w;

	i = 0;
	w = (char *)s;
	if (w[0] != c)
		i++;
	w = (ft_strchr(s, c));
	while (w != 0)
	{
		if (w[1] != c && w[1] != '\0')
			i++;
		w = ft_strchr(w + 1, c);
	}
	return (i);
}
